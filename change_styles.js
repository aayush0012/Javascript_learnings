/*
style.cssText in JavaScript

Used to apply multiple CSS styles to an element at once.

Syntax:
element.style.cssText = "property: value; property: value;";

Common styles:
background-color: red;
color: white;
padding: 10px;
margin: 10px;
border: 1px solid black;
font-size: 16px;
width: 100px;
height: 100px;

Notes:
- Use semicolons (;) between properties
- Do not use commas (,)
- It replaces all existing styles on the element
*/